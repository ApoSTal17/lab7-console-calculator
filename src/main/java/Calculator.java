public class Calculator {
    public int add(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int intDivision(int a, int b) {
        return a / b;
    }

    public float floatDivision(int a, int b) {
        return (float) a / b;
    }
}
